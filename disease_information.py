def get_disease_info(disease_name, language="en"):
    """Get disease information in specified language"""
    # Normalize disease name
    normalized_name = disease_name.replace(" ", "_").replace("-", "_")
    
    # Check if disease exists
    for key in disease_info:
        if normalized_name.lower() in key.lower():
            info = disease_info[key].get(language, disease_info[key].get("en"))
            return info
    
    # Return default if not found
    return {
        "cause": "Information not available",
        "prevention": "Information not available",
        "treatment": "Information not available",
        "steps": "Information not available",
        "fertilizers": "Information not available"
    }
    
# Export for import
DISEASE_INFO = disease_info
